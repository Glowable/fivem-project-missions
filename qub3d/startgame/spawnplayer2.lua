    TriggerEvent("mt:missiontext", "Select your ~b~Vehicle", 100000)
    Citizen.Wait(30000)
    TriggerEvent("qub3d:startsequence2")
end)

RegisterNetEvent("qub3d:cam2")
AddEventHandler("qub3d:cam2", function()
    print("created camera")
    cam = CreateCam("DEFAULT_SCRIPTED_CAMERA", true)
    SetCamCoord(cam, 3243.22, 1474.78, 307.95)
    SetCamRot(cam, 0.0, 0.0, 230.01, 0.0)
    SetCamActive(cam, true)
    RenderScriptCams(true, true, 1000, true, false)
    DisplayRadar(false)
    DisplayHud(false)
    Citizen.Wait(5000)
    TriggerEvent('cS.banner', "~o~qub3d~s~", "Knock the Enemy Players off the rotating Cube.", 5, true)

    Citizen.Wait(10000)
    DoScreenFadeOut(20)
    DisplayRadar(true)
    DisplayHud(true)
    RenderScriptCams(false, true, 1000, true, false)
    DestroyCam(cam, true)
    print("destroyed camera")
    Citizen.Wait(2000)
    DoScreenFadeIn(1000)
    Citizen.Wait(3000)
    TriggerEvent("qub3d:startsequence22")
end)