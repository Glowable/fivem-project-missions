    TriggerEvent("qub3d:spawnplayercar")
    TriggerEvent("qub3d:clouds2") 



end)

RegisterNetEvent("qub3d:clouds2")
AddEventHandler("qub3d:clouds2", function()
    Citizen.Wait(9500)
    StartPlayerSwitch(PlayerPedId(), PlayerPedId(), 255, 1)
    BeginTextCommandBusyspinnerOn("CUSTOMLOADSTRR")
    EndTextCommandBusyspinnerOn(4)
    Citizen.Wait(9000)
    TriggerEvent("qub3d:cam2")
    Citizen.Wait(2000) 
    BusyspinnerOff()
    StopPlayerSwitch()
end)

